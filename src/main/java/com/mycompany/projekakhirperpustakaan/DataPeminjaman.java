    private void isiCmb() {
        try {
            String sqlBuku = "SELECT Judul_Buku FROM buku WHERE Judul_Buku NOT IN (SELECT Judul FROM peminjaman WHERE Status = 'Dipinjam')";
            java.sql.PreparedStatement preparedStatementBuku = koneksi.getPreparedStatement(sqlBuku);
            ResultSet resultSetBuku = preparedStatementBuku.executeQuery();
            while (resultSetBuku.next()) {
                String judulBuku = resultSetBuku.getString("Judul_Buku");
                judul_txt.addItem(judulBuku);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan mengambil data:\n " + e.getMessage());
        }
        try {
            String sqlAnggota = "SELECT Nama FROM anggota WHERE Status <> 'Hitam'";
            java.sql.PreparedStatement preparedStatementAnggota = koneksi.getPreparedStatement(sqlAnggota);
            ResultSet resultSetAnggota = preparedStatementAnggota.executeQuery();

            while (resultSetAnggota.next()) {
                String namaAnggota = resultSetAnggota.getString("Nama");
                peminjam_txt.addItem(namaAnggota);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan mengambil data:\n " + e.getMessage());
        }
    }
    private void cariPeminjaman(String id) {
        DefaultTableModel grid = new DefaultTableModel();
        grid.addColumn("ID Peminjaman");
        grid.addColumn("Judul");
        grid.addColumn("Peminjam");
        grid.addColumn("Tanggal Pinjam");
        grid.addColumn("Status");
        try {
            String query = "SELECT * FROM peminjaman WHERE Peminjam LIKE ?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(query);
            preparedStatement.setString(1, "%" + id + "%");
            ResultSet rs = preparedStatement.executeQuery();
            while (rs.next()) {
                grid.addRow(new Object[]{
                    rs.getString(1), rs.getString(2), rs.getString(3), 
                    rs.getString(4), rs.getString(5)});
            }jTable1.setModel(grid);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal: " + e.getMessage());
        }
    }
