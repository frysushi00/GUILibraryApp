    private void perbaruidata_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_perbaruidata_btnActionPerformed
        String id_pinjam = author_txt.getText();
        String judul = judul_txt.getSelectedItem().toString();
        String peminjam = peminjam_txt.getSelectedItem().toString();
        String status = dipinjam_chk.isSelected() ? "Dipinjam" : (kembali_chk.isSelected() ? "Dikembalikan" : ""); 
        try {
                String sql = "UPDATE peminjaman SET Judul = ?, Peminjam = ?, Tanggal_Pinjam = ?, Status = ? " +
                             "WHERE ID_Peminjaman = ?";
                java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
                preparedStatement.setString(1, judul);
                preparedStatement.setString(2, peminjam);
                preparedStatement.setDate(3, new java.sql.Date(tgl_chs.getDate().getTime()));
                preparedStatement.setString(4, status);
                preparedStatement.setString(5, id_pinjam);
                preparedStatement.executeUpdate();
                tampil_peminjaman();
                JOptionPane.showMessageDialog(null, "Data Behasil Diperbarui!");
        } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, "Kesalahan Menyimpan Data!\n" + e.getMessage());
        }
        kosong();
    }//GEN-LAST:event_perbaruidata_btnActionPerformed

    private void hapus_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_btnActionPerformed
        int selectedRow = jTable1.getSelectedRow();
         if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih Baris yang Ingin Dihapus");
            return;
         }
         String id_pinjam = jTable1.getValueAt(selectedRow, 0).toString(); 

         try {
            String sql = "DELETE FROM peminjaman WHERE ID_Peminjaman = ?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
            preparedStatement.setString(1, id_pinjam);
            preparedStatement.executeUpdate();
            tampil_peminjaman();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
         } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menghapus Data: " + e.getMessage());
         }
    }//GEN-LAST:event_hapus_btnActionPerformed

