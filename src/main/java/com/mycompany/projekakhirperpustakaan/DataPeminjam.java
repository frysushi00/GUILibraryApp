        int selectedRow = jTable1user.getSelectedRow();
        if (selectedRow == -1) {
           JOptionPane.showMessageDialog(null, "Pilih Baris yang Ingin Diedit");
           return;
        }
        String id = jTable1user.getValueAt(selectedRow, 0).toString();
        String nama = jTable1user.getValueAt(selectedRow, 1).toString();
        String status = jTable1user.getValueAt(selectedRow, 2).toString();
