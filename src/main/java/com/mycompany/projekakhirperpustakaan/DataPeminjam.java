    private void perbaruidata_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_perbaruidata_btnActionPerformed
        String id = idAnggota_txt.getText();
        String nama = nama_txt.getText();
        String status = aktif_chk.isSelected() ? "Aktif" : (black_chk.isSelected() ? "Hitam" : "");

        try {
            String sql = "UPDATE anggota SET Nama=?, Status=? WHERE id_anggota=?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
            preparedStatement.setString(1, nama);
            preparedStatement.setString(2, status);
            preparedStatement.setString(3, id);
            preparedStatement.executeUpdate();
            tampil_peminjam();
            JOptionPane.showMessageDialog(null, "Data Behasil Diperbarui");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Inputan Salah\n" + e.getMessage());
        }
        kosong();
    }//GEN-LAST:event_perbaruidata_btnActionPerformed

    private void hapus_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_btnActionPerformed
        int selectedRow = jTable1user.getSelectedRow();
         if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih Baris yang Ingin Dihapus");
            return;
         }
         String id = jTable1user.getValueAt(selectedRow, 0).toString(); 

         try {
            String sql = "DELETE FROM anggota WHERE id_anggota = ?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
            preparedStatement.setString(1, id);
            preparedStatement.executeUpdate();
            tampil_peminjam();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
         } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menghapus Data: " + e.getMessage());
         }
    }//GEN-LAST:event_hapus_btnActionPerformed

