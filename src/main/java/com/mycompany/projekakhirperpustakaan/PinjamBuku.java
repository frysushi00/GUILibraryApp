    private void tampil_buku() {
        DefaultTableModel grid = new DefaultTableModel();
        grid.addColumn("ID Buku");
        grid.addColumn("Judul Buku");
        grid.addColumn("Pengarang");
        grid.addColumn("Tahun Terbit");
        grid.addColumn("Genre");
        try {
            ResultSet rs = koneksi.getStatement().executeQuery("SELECT * FROM buku");
            while (rs.next()) {
                grid.addRow(new Object[]{
                        rs.getString(1), rs.getString(2), rs.getString(3),
                        rs.getString(4), rs.getString(5)});
            }
            jTable1buku.setModel(grid);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal: " + e.getMessage());
        }
    }
