    private void hapus_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_btnActionPerformed
         int selectedRow = jTable1buku.getSelectedRow();
         if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih Baris yang Ingin Dihapus");
            return;
         }
         String judul = jTable1buku.getValueAt(selectedRow, 0).toString(); 

         try {
            String sql = "DELETE FROM buku WHERE ID_Buku = ?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
            preparedStatement.setString(1, judul);
            preparedStatement.executeUpdate();
            tampil_buku();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
         } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menghapus Data: " + e.getMessage());
         }
    }//GEN-LAST:event_hapus_btnActionPerformed

    private void perbaruiBuku_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_perbaruiBuku_btnActionPerformed
        String id = id_txt.getText();
        String judul = judul_txt.getText();
        String pengarang = pengarang_txt.getText();
        String tahun = tahun_txt.getText();
        String genre = (String) genre_cmb.getSelectedItem();
        try {
            String sql = "UPDATE buku SET Judul_Buku=?, Pengarang=?, Tahun_Terbit=?, Genre=? WHERE ID_Buku=?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(sql);
            preparedStatement.setString(1, judul);
            preparedStatement.setString(2, pengarang);
            preparedStatement.setString(3, tahun);
            preparedStatement.setString(4, genre);
            preparedStatement.setString(5, id);
            preparedStatement.executeUpdate();
            tampil_buku();
            JOptionPane.showMessageDialog(null, "Data Behasil Diperbarui");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Inputan Salah\n" + e.getMessage());
        }
        kosong();
    }//GEN-LAST:event_perbaruiBuku_btnActionPerformed

