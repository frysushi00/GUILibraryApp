    private void cariBuku(String idCari, String judulCari, String genreCari) {
        DefaultTableModel grid = new DefaultTableModel();
        grid.addColumn("ID Buku");
        grid.addColumn("Judul");
        grid.addColumn("Pengarang");
        grid.addColumn("Tahun Terbit");
        grid.addColumn("Genre");
        try {
            String query = "SELECT * FROM buku WHERE ID_Buku LIKE ? OR Judul_Buku LIKE ? OR Genre LIKE ?";
            java.sql.PreparedStatement preparedStatement = koneksi.getPreparedStatement(query);
            preparedStatement.setString(1, "%" + idCari + "%");
            preparedStatement.setString(2, "%" + judulCari + "%");
            preparedStatement.setString(3, "%" + genreCari + "%");
            ResultSet rs = preparedStatement.executeQuery();
            while (rs.next()) {
                grid.addRow(new Object[]{
                    rs.getString(1), rs.getString(2), rs.getString(3),
                    rs.getString(4), rs.getString(5)});
            } jTable1buku.setModel(grid);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal: " + e.getMessage());
        }
    }
